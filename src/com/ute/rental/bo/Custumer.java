package com.ute.rental.bo;

public class Custumer extends Users {
	private int custumerid;
	private int userid;
	private String fullnName;
	private String idcard;
	private String dateBrith;
	private String email;
	private String address;
	private String phoneNumber;
	
	public int getCustumerid() {
		return custumerid;
	}
	public void setCustumerid(int custumerid) {
		this.custumerid = custumerid;
	}
	public int getUserid() {
		return userid;
	}
	public void setUserid(int userid) {
		this.userid = userid;
	}
	public String getFullnName() {
		return fullnName;
	}
	public void setFullnName(String fullnName) {
		this.fullnName = fullnName;
	}
	public String getIdcard() {
		return idcard;
	}
	public void setIdcard(String idcard) {
		this.idcard = idcard;
	}
	public String getDateBrith() {
		return dateBrith;
	}
	public void setDateBrith(String dateBrith) {
		this.dateBrith = dateBrith;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhoneNumber() {
		return phoneNumber;
	}
	public void setPhoneNumber(String phoneNumber) {
		this.phoneNumber = phoneNumber;
	}
	
		
	
	
}
